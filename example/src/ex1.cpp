#include <stdio.h> 

int main()
{
  //연산자 : 산술 연산자 + - * / %

  int a = 5;
  int b = 3;
  int result = 0;

  result = a + b;
  printf(" %d \n", result);
  result = a - b;
  printf(" %d \n", result);
  result = a * b;
  printf(" %d \n", result);
  result = a / b;
  printf(" %d \n", result);
  result = a % b;
  printf(" %d \n", result);
}

